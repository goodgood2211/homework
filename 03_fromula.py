# 3. 写程序求多项式的和:
#   1/1 - 1/3 + 1/5 - 1/7 + 1/9 ... + 1/(2*n-1) 的和
#   n最大取： 1000000
#   1) 打印这个和
#   2) 打印这个和乘以4的值?(看看这个数眼熟不?)


n = 1  # i代表分母
sign = 1
s = 0  # 用于累加多项式的和
while n <= 10000000:
    s += sign * 1 / (2 * n - 1)
    sign = sign * -1  # 将多项式的正负号改变
    n += 1

print("和是:", s)
print("s x 4 =", s * 4)
